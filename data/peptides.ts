export function getPeptideById(id: string): Peptide | undefined {
  return peptides.find(p => p.id === id);
}

export function searchPeptides(query: string): Peptide[] {
  const q = query.toLowerCase();
  return peptides.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.abbreviation.toLowerCase().includes(q) ||
    p.tags.some(t => t.includes(q)) ||
    p.effects.some(e => e.toLowerCase().includes(q)) ||
    p.category.includes(q) ||
    p.description.toLowerCase().includes(q)
  );
}

export function getSynergyScore(peptideA: string, peptideB: string): number {
  const a = getPeptideById(peptideA);
  const b = getPeptideById(peptideB);
  if (!a || !b) return 0;

  let score = 0;
  if (a.synergisticWith.includes(b.id)) score += 4;
  if (b.synergisticWith.includes(a.id)) score += 4;
  if (a.conflictsWith.includes(b.id) || b.conflictsWith.includes(a.id)) score -= 8;
  if (a.category === b.category) score += 1;
  if (a.tags.some(t => b.tags.includes(t))) score += 1;

  return Math.max(0, Math.min(10, score));
}

export function getStackCompatibility(peptideIds: string[]): { score: number; conflicts: string[][]; synergies: string[][] } {
  const conflicts: string[][] = [];
  const synergies: string[][] = [];
  let totalScore = 0;
  let pairs = 0;

  for (let i = 0; i < peptideIds.length; i++) {
    for (let j = i + 1; j < peptideIds.length; j++) {
      const score = getSynergyScore(peptideIds[i], peptideIds[j]);
      totalScore += score;
      pairs++;
      
      const a = getPeptideById(peptideIds[i]);
      const b = getPeptideById(peptideIds[j]);
      if (a && b) {
        if (a.conflictsWith.includes(b.id) || b.conflictsWith.includes(a.id)) {
          conflicts.push([a.name, b.name]);
        }
        if (a.synergisticWith.includes(b.id) || b.synergisticWith.includes(a.id)) {
          synergies.push([a.name, b.name]);
        }
      }
    }
  }

  return {
    score: pairs > 0 ? Math.round(totalScore / pairs * 10) / 10 : 0,
    conflicts,
    synergies,
  };
}
